"use client";

import { motion } from "framer-motion";
import { X } from "lucide-react";
import { useState } from "react";
import { toast } from "react-toastify";
import { Button } from "./Button";

type AddMemberModalProps = {
	isOpen: boolean;
	onClose: () => void;
	repoId: string;
};

export function AddMemberModal({
	isOpen,
	onClose,
	repoId,
}: AddMemberModalProps) {
	const [email, setEmail] = useState("");
	const [loading, setLoading] = useState(false);

	const handleSubmit = async (e: React.FormEvent) => {
		e.preventDefault();
		setLoading(true);

		try {
			const res = await fetch(`/api/repos/${repoId}/invitations`, {
				method: "POST",
				headers: { "Content-Type": "application/json" },
				body: JSON.stringify({ email }),
			});

			if (!res.ok) {
				const data = await res.json();
				throw new Error(data?.error || "Failed to send invite");
			}

			toast.success(`Invitation sent to ${email}!`);
			setEmail("");
		} catch (err) {
			console.log("Error sending email invite: ", err);
			toast.error(
				(err instanceof Error && err.message) ||
					"We can't send your invite for now... Please try again later.",
			);
		} finally {
			setLoading(false);
		}
	};

	if (!isOpen) return null;

	return (
		<div className="fixed backdrop-blur-sm inset-0 z-50 flex items-center justify-center bg-black/50">
			<motion.div
				initial={{ opacity: 0, scale: 0.5 }}
				animate={{ opacity: 1, scale: 1 }}
				exit={{ opacity: 0, scale: 0.5 }}
				className="pointer-events-auto w-full max-w-sm rounded-xl bg-white dark:bg-zinc-950/90 border border-gray-200 dark:border-gray-800 p-6 shadow-lg"
			>
				<div className="flex justify-between items-center mb-6">
					<h2 className="text-xl font-semibold">Invite member</h2>
					<button
						className="hover:cursor-pointer hover:opacity-70 transition-colors"
						type="button"
						onClick={onClose}
					>
						<X />
					</button>
				</div>

				<form onSubmit={handleSubmit} className="space-y-6">
					<input
						required
						type="email"
						placeholder="user@email.com"
						className="w-full border border-gray-300 dark:border-gray-700 rounded-lg p-2 bg-white dark:bg-zinc-950 focus:outline-none"
						value={email}
						onChange={(e) => setEmail(e.target.value)}
						disabled={loading}
					/>

					<Button
						className="w-full h-9 rounded-lg bg-gray-900 text-white dark:bg-gray-200 dark:text-black hover:cursor-pointer hover:opacity-90 disabled:opacity-50"
						disabled={loading}
					>
						{loading ? "Sending..." : "Send invite"}
					</Button>
				</form>
			</motion.div>
		</div>
	);
}
